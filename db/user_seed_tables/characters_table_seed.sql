    id serial primary key,
    user_id integer not null references users(id),
    name varchar(180),
    image text,
    race varchar(20),
    class varchar(20),
    level integer default 1,
    experience_points integer,
    background varchar(20),
    alignment varchar(20),
    strength integer,
    dexterity integer,
    constitution integer,
    intelligence integer,
    wisdom integer,
    charisma integer,
    inspiration integer,
    proficiency_bonus integer,
    strength_saving_throw integer,
    dexterity_saving_throw integer,
    constitution_saving_throw integer,
    intelligence_saving_throw integer,
    wisdom_saving_throw integer,
    charisma_saving_throw integer,
    acrobatics integer,
    animal_handling integer,
    arcana integer,
    athletics integer,
    deception integer,
    history integer,
    insight integer,
    intimidation integer,
    investigation integer,
    medicine integer,
    nature integer,
    perception integer,
    performance integer,
    persuasion integer,
    religion integer,
    sleight_of_hand integer,
    stealth integer,
    survival integer,
    passive_wisdom integer,
    armor_class integer,
    initiative integer,
    speed integer,
    max_hit_points integer,
    current_hit_points integer,
    temp_hit_points integer,
    total_hit_dice varchar(10),
    current_hit_dice integer,
    personality_traits varchar(180),
    ideals varchar(180),
    bonds varchar(180),
    flaws varchar(180),
    gold integer,
    silver integer,
    copper integer
);
